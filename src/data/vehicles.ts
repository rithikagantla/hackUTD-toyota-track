// Helper functions for filtering
export function getVehiclesByFuelType(fuelType: FuelType | 'any'): Vehicle[] {
  if (fuelType === 'any') return vehicles
  return vehicles.filter((v) => v.fuelType === fuelType)
}

export function getVehiclesByBodyStyle(bodyStyle: BodyStyle | 'any'): Vehicle[] {
  if (bodyStyle === 'any') return vehicles
  return vehicles.filter((v) => v.bodyStyle === bodyStyle)
}

export function getVehiclesByPriceRange(min: number, max: number): Vehicle[] {
  return vehicles.filter((v) => v.msrp >= min && v.msrp <= max)
}

export function getVehicleById(id: string): Vehicle | undefined {
  return vehicles.find((v) => v.id === id)
}

export function searchVehicles(query: string): Vehicle[] {
  const lowerQuery = query.toLowerCase()
  return vehicles.filter(
    (v) =>
      v.name.toLowerCase().includes(lowerQuery) ||
      v.description.toLowerCase().includes(lowerQuery) ||
      v.features.some((f) => f.toLowerCase().includes(lowerQuery)) ||
      v.tags.some((t) => t.toLowerCase().includes(lowerQuery))
  )
}
